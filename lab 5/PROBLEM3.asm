ORG 0100H
.DATA
N DB 11011011B
RESULT DB ?
.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX

    MOV AL,N
    MOV BL,N
    MOV CL,4
CHECK:
    ROL AL,1
    JC ONEL
    MOV AH,0
ZEROL:
    ROR BL,1
    JC ONER
    MOV BH,0
ZEROR:
    CMP AH,BH
    JNE NOT_PALINDROME
    DEC CL
    JNZ CHECK
    JMP PALINDROME
ONEL:
    MOV AH,1
    JMP ZEROL
ONER:
    MOV BH,1
    JMP ZEROR
    
PALINDROME:
    MOV BL,0AAH
    JMP END
NOT_PALINDROME:
    MOV BL,00H
END:
    MAIN ENDP
END MAIN
RET